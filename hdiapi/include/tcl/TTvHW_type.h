#ifndef _TTvHW_TYPE_H_
#define _TTvHW_TYPE_H_

/// DTV
typedef unsigned char       TAPI_BYTE;  // 1 byte
typedef unsigned char       TAPI_BOOL;  // 1 byte
typedef signed char         TAPI_S8;    // 1 byte
typedef signed short        TAPI_S16;   // 2 bytes
typedef signed long         TAPI_S32;   // 4 bytes
typedef signed long long    TAPI_S64;   // 8 bytes
typedef unsigned char       TAPI_U8;    // 1 byte
typedef unsigned short      TAPI_U16;   // 2 bytes
typedef unsigned long       TAPI_U32;   // 4 bytes
typedef unsigned long long  TAPI_U64;   // 8 bytes
typedef unsigned short      TAPI_WORD;  // 2 bytes
#define TAPI_TRUE 1 // true
#define TAPI_FALSE 0 // false



#endif //_TTvHW_TYPE_H_
